CREATE TABLE core.group_identifier (
    group_identifier_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id BIGINT NOT NULL,
    identifier_type TEXT NOT NULL,
    identifier_value TEXT NOT NULL,
    CONSTRAINT fk_group_identifier_group
        FOREIGN KEY (group_id)
        REFERENCES core.group_record (group_id)
        ON DELETE CASCADE,
    CONSTRAINT uq_group_identifier
        UNIQUE (group_id, identifier_type, identifier_value)
);

CREATE TABLE core.contact (
    contact_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    establishment_id BIGINT,
    group_id BIGINT,
    website TEXT,
    telephone_number TEXT,
    CONSTRAINT fk_contact_establishment
        FOREIGN KEY (establishment_id)
        REFERENCES core.establishment (establishment_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_contact_group
        FOREIGN KEY (group_id)
        REFERENCES core.group_record (group_id)
        ON DELETE CASCADE,
    CONSTRAINT chk_contact_owner
        CHECK (establishment_id IS NOT NULL OR group_id IS NOT NULL)
);

CREATE TABLE core.establishment_provision (
    establishment_provision_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    establishment_id BIGINT NOT NULL UNIQUE,
    education_phase_id BIGINT,
    nursery_provision_id BIGINT,
    official_sixth_form_id BIGINT,
    further_education_type_id BIGINT,
    fsm INTEGER,
    percentage_fsm NUMERIC(5,2),
    CONSTRAINT fk_establishment_provision_establishment
        FOREIGN KEY (establishment_id)
        REFERENCES core.establishment (establishment_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_establishment_provision_education_phase
        FOREIGN KEY (education_phase_id)
        REFERENCES ref.education_phase (education_phase_id),
    CONSTRAINT chk_establishment_provision_percentage_fsm
        CHECK (percentage_fsm IS NULL OR percentage_fsm BETWEEN 0 AND 100)
);

-- ------------------------------------------------------------
-- Lifecycle Tables
-- ------------------------------------------------------------
CREATE TABLE core.establishment_status_history (
    establishment_status_history_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    establishment_id BIGINT NOT NULL,
    old_status_id BIGINT,
    new_status_id BIGINT NOT NULL,
    changed_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT fk_establishment_status_history_establishment
        FOREIGN KEY (establishment_id)
        REFERENCES core.establishment (establishment_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_establishment_status_history_old_status
        FOREIGN KEY (old_status_id)
        REFERENCES ref.establishment_status (establishment_status_id),
    CONSTRAINT fk_establishment_status_history_new_status
        FOREIGN KEY (new_status_id)
        REFERENCES ref.establishment_status (establishment_status_id)
);

CREATE TABLE core.establishment_lifecycle_event (
    establishment_lifecycle_event_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    establishment_id BIGINT NOT NULL,
    event_type TEXT NOT NULL,
    opened_reason_id BIGINT,
    closed_reason_id BIGINT,
    event_date DATE NOT NULL,
    CONSTRAINT fk_establishment_lifecycle_event_establishment
        FOREIGN KEY (establishment_id)
        REFERENCES core.establishment (establishment_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_establishment_lifecycle_event_opened_reason
        FOREIGN KEY (opened_reason_id)
        REFERENCES ref.reason_establishment_opened (reason_establishment_opened_id),
    CONSTRAINT fk_establishment_lifecycle_event_closed_reason
        FOREIGN KEY (closed_reason_id)
        REFERENCES ref.reason_establishment_closed (reason_establishment_closed_id)
);


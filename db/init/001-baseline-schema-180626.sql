-- ------------------------------------------------------------
-- Schemas
-- ------------------------------------------------------------

CREATE SCHEMA IF NOT EXISTS ref;
CREATE SCHEMA IF NOT EXISTS core;


-- ------------------------------------------------------------
-- Reference tables
-- ------------------------------------------------------------
CREATE TABLE ref.establishment_family (
    establishment_family_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code TEXT NOT NULL UNIQUE,
    name TEXT NOT NULL,
    description TEXT,
    is_active BOOLEAN NOT NULL DEFAULT TRUE
);

CREATE TABLE ref.establishment_type (
    establishment_type_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    establishment_family_id BIGINT NOT NULL,
    code TEXT NOT NULL UNIQUE,
    name TEXT NOT NULL,
    is_school BOOLEAN NOT NULL DEFAULT FALSE,
    is_group BOOLEAN NOT NULL DEFAULT FALSE,
    is_early_years BOOLEAN NOT NULL DEFAULT FALSE,
    is_further_education BOOLEAN NOT NULL DEFAULT FALSE,
    CONSTRAINT fk_establishment_type_family
        FOREIGN KEY (establishment_family_id)
        REFERENCES ref.establishment_family (establishment_family_id)
);

CREATE TABLE ref.establishment_status (
    establishment_status_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code TEXT NOT NULL UNIQUE,
    name TEXT NOT NULL,
    description TEXT,
    is_active BOOLEAN NOT NULL DEFAULT TRUE
);

CREATE TABLE ref.education_phase_group (
    education_phase_group_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code TEXT NOT NULL UNIQUE,
    name TEXT NOT NULL
);

CREATE TABLE ref.education_phase (
    education_phase_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    education_phase_group_id BIGINT NOT NULL,
    code TEXT NOT NULL UNIQUE,
    name TEXT NOT NULL,
    CONSTRAINT fk_education_phase_group
        FOREIGN KEY (education_phase_group_id)
        REFERENCES ref.education_phase_group (education_phase_group_id)
);

CREATE TABLE ref.group_type (
    group_type_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code TEXT NOT NULL UNIQUE,
    name TEXT NOT NULL
);

CREATE TABLE ref.role_type (
    role_type_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code TEXT NOT NULL UNIQUE,
    name TEXT NOT NULL
);

CREATE TABLE ref.title (
    title_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE
);

CREATE TABLE ref.reason_establishment_opened (
    reason_establishment_opened_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code TEXT NOT NULL UNIQUE,
    name TEXT NOT NULL
);

CREATE TABLE ref.reason_establishment_closed (
    reason_establishment_closed_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code TEXT NOT NULL UNIQUE,
    name TEXT NOT NULL
);

-- ------------------------------------------------------------
-- Core parent tables
-- ------------------------------------------------------------
CREATE TABLE core.establishment (
    establishment_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    urn TEXT UNIQUE,
    uid TEXT UNIQUE,
    name TEXT NOT NULL,
    establishment_number TEXT,
    establishment_type_id BIGINT NOT NULL,
    establishment_status_id BIGINT NOT NULL,
    headteacher_role_assignment_id BIGINT,
    CONSTRAINT fk_establishment_type
        FOREIGN KEY (establishment_type_id)
        REFERENCES ref.establishment_type (establishment_type_id),
    CONSTRAINT fk_establishment_status
        FOREIGN KEY (establishment_status_id)
        REFERENCES ref.establishment_status (establishment_status_id)
);

CREATE TABLE core.group_record (
    group_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code TEXT UNIQUE,
    name TEXT NOT NULL,
    group_type_id BIGINT NOT NULL,
    headteacher_role_assignment_id BIGINT,
    CONSTRAINT fk_group_record_group_type
        FOREIGN KEY (group_type_id)
        REFERENCES ref.group_type (group_type_id)
);

CREATE TABLE core.person (
    person_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title_id BIGINT,
    given_name TEXT,
    family_name TEXT,
    display_name TEXT,
    CONSTRAINT fk_person_title
        FOREIGN KEY (title_id)
        REFERENCES ref.title (title_id)
);

CREATE TABLE core.role (
    role_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    person_id BIGINT NOT NULL,
    role_type_id BIGINT NOT NULL,
    CONSTRAINT fk_role_person
        FOREIGN KEY (person_id)
        REFERENCES core.person (person_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_role_role_type
        FOREIGN KEY (role_type_id)
        REFERENCES ref.role_type (role_type_id)
);

-- ------------------------------------------------------------
-- Relationship tables
-- ------------------------------------------------------------
CREATE TABLE core.role_assignment (
    role_assignment_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    role_id BIGINT NOT NULL,
    establishment_id BIGINT,
    group_id BIGINT,
    preferred_job_title TEXT,
    CONSTRAINT fk_role_assignment_role
        FOREIGN KEY (role_id)
        REFERENCES core.role (role_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_role_assignment_establishment
        FOREIGN KEY (establishment_id)
        REFERENCES core.establishment (establishment_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_role_assignment_group
        FOREIGN KEY (group_id)
        REFERENCES core.group_record (group_id)
        ON DELETE CASCADE,
    CONSTRAINT chk_role_assignment_scope
        CHECK (establishment_id IS NOT NULL OR group_id IS NOT NULL)
);

CREATE TABLE core.establishment_group_membership (
    establishment_group_membership_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    establishment_id BIGINT NOT NULL,
    group_id BIGINT NOT NULL,
    membership_category TEXT,
    membership_flag TEXT,
    start_date DATE,
    end_date DATE,
    CONSTRAINT fk_establishment_group_membership_establishment
        FOREIGN KEY (establishment_id)
        REFERENCES core.establishment (establishment_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_establishment_group_membership_group
        FOREIGN KEY (group_id)
        REFERENCES core.group_record (group_id)
        ON DELETE CASCADE,
    CONSTRAINT chk_establishment_group_membership_dates
        CHECK (end_date IS NULL OR start_date IS NULL OR end_date >= start_date)
);

CREATE TABLE core.group_group_membership (
    parent_group_id BIGINT NOT NULL,
    child_group_id BIGINT NOT NULL,
    PRIMARY KEY (parent_group_id, child_group_id),
    CONSTRAINT fk_group_group_membership_parent
        FOREIGN KEY (parent_group_id)
        REFERENCES core.group_record (group_id)
        ON DELETE CASCADE,
    CONSTRAINT fk_group_group_membership_child
        FOREIGN KEY (child_group_id)
        REFERENCES core.group_record (group_id)
        ON DELETE CASCADE,
    CONSTRAINT chk_group_group_membership_not_self
        CHECK (parent_group_id <> child_group_id)
);

